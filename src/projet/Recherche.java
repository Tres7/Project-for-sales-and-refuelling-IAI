    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int index=Table.getSelectedRow();
        Article Art = new Article();
        Art.setLibelle(Lib.getText());
        new Enregistrer().rechercher(Art);
        Connection cone= Connexion.SeConnecter();
        String requete="Select * FROM article where Libelle='"+Lib.getText()+"'";
        List<Article> liste_article = new ArrayList<Article>() ;
        ResultSet rs = null;
        DefaultTableModel tableau=null;
        try{
            PreparedStatement ps=cone.prepareStatement(requete);
            rs=ps.executeQuery(requete);
            while (rs.next()){
                Article art=new Article();
                art.setCode(rs.getString("Code"));
                art.setLibelle(rs.getString("Libelle"));
                art.setPrix(rs.getInt("Prix"));
                art.setQuantite(rs.getInt("Quantite"));
                art.setDate(rs.getDate("date"));
                
                liste_article.add(art);
            }
            String en_tete[]= {"Code","Libelle","Prix","Quantite","date"};
            tableau=new DefaultTableModel();                                        
            tableau.setColumnIdentifiers(en_tete);
            for (Article a : liste_article) {  
            String b=String.valueOf(a.getCode());
            String c=String.valueOf(a.getPrix());
            String d=String.valueOf(a.getQuantite());
            String  tabdata[] = {b,a.getLibelle(),c,d,a.getDate().toString()};
            tableau.addRow(tabdata);
            
        }
        
        }catch(SQLException ex){
            //Logger.getLogger(Enregistrer.class.getName()).log(Level.SEVERE,null,ex);
            System.err.println(ex.getMessage());
            
        
        }
        //ras();
        Table.setModel(tableau);
        //TxtNbr.setText(new Enregistrer().nbrIns()+"");// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

